package com.clemens.version;

public class Version {

    private static final String versionType = "v";
    private static final double versionMajor = 1;
    private static final double versionMinor = 0;
    private static final double versionPatch = 1;
    public static final String version = versionType + versionMajor + "." + versionMinor + "." + versionPatch;

}
